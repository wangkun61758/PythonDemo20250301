def test_write():
    data = ["李白", "杜甫", "白居易", "王维"]
    if not os.path.exists('../../resources/csv/'):
        os.makedirs('../../resources/csv/')
    try:
        str6 = str(random.randint(1000, 9999)) + '.csv'
        with open('../../resources/csv/' + str6, 'w', encoding='utf-8') as file6:
            writer6 = csv.writer(file6)
            writer6.writerow(data)
    finally:
        print('完成')
